#include<stdio.h>

int main(){
    int a = 10;
    printf("%d \n", a);

    float b = (float)a;
    printf("%f \n", b);

    char ch = 'A';
    printf("%c \n", ch);

    int ascii = (int)ch;
    printf("%d \n", ascii);

    int x = 97;
    printf("%c \n",x);

    int zero = 48;
    printf("%d \n", zero);

    char z = (char)zero;
    printf("%c \n", z);
    return 0;
}